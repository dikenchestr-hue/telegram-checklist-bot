@dp.message(lambda message: message.text == "🚀 Старт")
async def process_start_button(message: types.Message, state: FSMContext):
    # Создаем клавиатуру для выбора типа статьи (компактные названия)
    keyboard = ReplyKeyboardMarkup(
        keyboard=[
            [KeyboardButton(text="🔬 Оригинальная")],
            [KeyboardButton(text="📚 Обзорная")]
        ],
        resize_keyboard=True
    )
    
    await message.answer(
        "**Выберите тип вашей статьи:**\n🔬 Оригинальная\n📚 Обзорная",
        reply_markup=keyboard,
        parse_mode="Markdown"
    )
    await state.set_state(ChecklistStates.choosing_article_type)

