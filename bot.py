async def handle_all_questions(message: types.Message, state: FSMContext):
    # Получаем текущее состояние
    current_state = await state.get_state()
    
    # Извлекаем номер вопроса из состояния (например, 'review_q1' -> 1)
    parts = current_state.split('_q')
    question_num = int(parts[1])
    
    # Проверяем ответ
    if message.text not in ["✅ Да", "❌ Нет"]:
        await message.answer(
            "Пожалуйста, используйте кнопки **✅ Да** или **❌ Нет**",
            reply_markup=get_yes_no_keyboard(),
            parse_mode="Markdown"
        )
        return
    
    # Сохраняем ответ
    user_id = message.from_user.id
    if user_id not in user_answers:
        await message.answer("Произошла ошибка. Начните заново с /start")
        await state.clear()
        return
    
    answer = "да" if message.text == "✅ Да" else "нет"
    user_answers[user_id]['answers'][f"q{question_num}"] = answer
    
    # Получаем общее количество вопросов и категорию
    total_questions = user_answers[user_id]['total']
    category = user_answers[user_id]['category']
    
    # Проверяем, есть ли следующий вопрос
    if question_num < total_questions:
        # Переходим к следующему вопросу
        next_state_name = f"{category}_q{question_num + 1}"
        try:
            next_state = getattr(ChecklistStates, next_state_name)
        except AttributeError:
            # Если следующего состояния нет, показываем результаты
            await show_results(message, user_answers[user_id])
            await state.clear()
            del user_answers[user_id]
            await message.answer(
                "✨ **Чек-лист завершен!**\n\nЧтобы начать заново, отправьте /start",
                reply_markup=ReplyKeyboardRemove(),
                parse_mode="Markdown"
            )
            return
        
        questions = user_answers[user_id]['questions']
        await message.answer(
            questions[question_num],  # индекс = question_num (0-based)
            reply_markup=get_yes_no_keyboard(),
            parse_mode="Markdown"
        )
        await state.set_state(next_state)
    else:
        # Это был последний вопрос - показываем результаты
        await show_results(message, user_answers[user_id])
        await state.clear()
        del user_answers[user_id]
        
        await message.answer(
            "✨ **Чек-лист завершен!**\n\nЧтобы начать заново, отправьте /start",
            reply_markup=ReplyKeyboardRemove(),
            parse_mode="Markdown"
        )

